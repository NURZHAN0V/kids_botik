def функция_угадайка():
    import random
    import os

    def очистить_экран():
        os.system('cls' if os.name == 'nt' else 'clear')

    ещё = "да"
    while ещё.lower() == "да":
        случайноеЧисло = random.randint(1, 10)
        попытки = 0
        print("ИИ: Я загадал число от 1 до 10. Попробуй угадать!")

        числоПользователя = None

        while числоПользователя != случайноеЧисло:
            очистить_экран()
            print(f"Попытка №{попытки + 1}")
            ввод = input("ИИ: Какое число я загадал?\nЯ: ")

            if not ввод.isdigit():
                print("⚠️ Введи число, а не что-то другое!")
                input("Нажми Enter, чтобы продолжить...")
                continue

            числоПользователя = int(ввод)
            попытки += 1

            if числоПользователя != случайноеЧисло:
                print("❌ Не верно. Попробуй еще раз.")
                input()

        очистить_экран()
        print(f"✅ Молодец! Ты угадал с {попытки} попытки. Моё число: {случайноеЧисло}")

        ещё = input("Хочешь сыграть ещё раз? (да/нет): ")

    print("Спасибо за игру! До новых встреч!")